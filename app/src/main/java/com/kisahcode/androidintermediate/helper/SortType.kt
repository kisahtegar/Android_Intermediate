package com.kisahcode.androidintermediate.helper

/**
 * An enumeration representing different types of sorting options.
 */
enum class SortType {
    /**
     * Sorts the data in ascending order.
     */
    ASCENDING,

    /**
     * Sorts the data in descending order.
     */
    DESCENDING,

    /**
     * Sorts the data in random order.
     */
    RANDOM
}